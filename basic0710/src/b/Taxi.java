package b;

public class Taxi {

	private double korea;
	private int total;
	private float math;
	private float eng;
	private float avg;
	
	void setKorea(double korea) {
		this.korea=korea;
	}
	
	void setTotal(int total) {
		this.total=total;
	}
	void setMath(float math) {
		this.math=math;
	}
	void setEng(float eng) {
		this.eng=eng;
	}
	void setAvg(float avg) {
		this.avg=avg;
	}
	double getKorea() {
		return korea;
		
	}
	int getTotal() {
		return total;
	}
	float getMath() {
		return math;
	}
	float getEng() {
		return eng;
	}
	float getAvg() {
		return avg;
	}
	
}
