package b;

public class Bus {
	private int uu;
	private String vv;
	private float zk;
	private String ru;
	


	void setuu(int uu) {
		this.uu=uu;
	}
	void setvv(String vv) {
		this.vv=vv;
	}
	void setru(String ru) {
		this.ru=ru;
	}
	void setzk(float zk) {
		this.zk=zk;
	}


	int getuu() {
		return this.uu;
	}
	
	String getvv() {
		return this.vv;
	}
	String getru() {
		return this.ru;
	}
	
	float getzk() {
		return this.zk;
	}
}